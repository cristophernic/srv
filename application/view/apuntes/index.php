                 $("#tabla\\.resultado").empty();
                 $.each(data, function(i, item) {
                     let fila = '<tr><th scope="row">';
         
                     fila += item["apunte_id"] + '</th><td>';
                     fila += item["apunte_date"] + '</td><td>';
                     fila += item["apunte_person"] + '</td><td>';
                     fila += item["apunte_keywords"] + '</td></tr>';
         
